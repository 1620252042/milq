# LOOPS


print('CLASSICAL LOOP')  # CLASSICAL LOOP

for i in range(10):
    print('i:', i)




print('CLASSICAL LOOP WITH ARRAYS')  # CLASSICAL LOOP WITH ARRAYS

numbers = [-3.2, 5.3, 3, 1]

for i in range(len(numbers)):
    print(numbers[i])




print('REVERSE LOOP')  # REVERSE LOOP

for i in range(5, -1, -1):
    print('i:', i)




print('NESTED LOOP')  # NESTED LOOP

for i in range(3):
    print('i:', i)
    for j in range(2, 0, -1):
        print('j:', j)
        for k in range(2):
            print('k:', k)






print('ITERATOR LOOP')  # ITERATOR LOOP

colors = ['red', 'green', 'blue', 'yellow']

for color in colors:
    print(color)




print('WHILE LOOP')  # WHILE LOOP

i = 0
while i <= 3:
    i = i + 1
    print('Hello', i)













